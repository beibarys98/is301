# Даны целые положительные числа A и B (A > B).
# На отрезке длины A размещено максимальное возможное количество отрезков длины B (без наложений).
# Используя операцию деления нацело, найти количество отрезков B, размещённых на отрезке A.

A = int(input("Введите длину отрезка A: "))
B = int(input("Введите длину отрезка B (A > B): "))

count = A // B

print("Количество отрезков B, размещённых на отрезке A:", count)