a = int(input("Введите длину прямоугольника a: "))
b = int(input("Введите ширину прямоугольника b: "))
c = int(input("Введите высоту прямоугольника c: "))
v = a*b*c
s = 2 * (a*b + a*c + b*c)
print("Объем прямоугольного параллелепипеда V =", v) 
print("Площадь поверхности прямоугольного параллелепипеда S =", s)
       